n=int(input("enter a number pls : "))
prime_list=[]
for say覺 in range(2,n):
  counter=0
  for i in range(2,say覺):
    if say覺 % i==0:
      counter += 1 
  if counter ==0:
    prime_list.append(say覺)
print(f"From 1 to {n} prime_number_list is {prime_list}")